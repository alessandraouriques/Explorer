// OPERADORES DE COMPARAÇÃO MAIOR E MENOR (IGUAL)

// Maior que: >
// Maior igual a: >=
// Menor que: <
// Menor igual a: <=


let one = 1
let two = 2

// Maior que >
console.log(one > two)

// Maior igual a >= 
console.log(one >= 1)
console.log(two > 1)

// Menor que
console.log(one < two)

// Menor igual a 
console.log (one <= two)
console.log (one <= 1)
console.log(one <= 0)

/* DESAFIO
  Solicite 2 números, faça a soma deles e
  apresente o resultado final ao usuário
*/

alert("Iremos somar 2 números");

let numberOne = prompt("Digite um número para a soma");
let numberTwo = prompt("Digite outro número para a soma");
let resultado = Number(numberOne) + Number(numberTwo);
alert("O resultado da soma é: " + resultado)



